#Create four math functions, pass each one two arguments, and do what the name says.
def sum0(num1,num2):
    print(num1 + num2)
def sum1(num1,num2):
    print(num1 - num2)
def sum2(num1,num2):
    print(num1 * num2)
def sum3(num1,num2):
    print(num1 / num2)

#Operations: +, -, *, /.
sum0(2,4)
sum1(3,7)
sum2(1,3)
sum3(6,9)